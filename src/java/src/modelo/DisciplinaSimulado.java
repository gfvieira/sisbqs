package src.modelo;

/**
 *
 * @author root
 */
public class DisciplinaSimulado {
    
    private int id_disciplina;
    private String disciplina;
    private int id_assunto;
    private String assunto;

    /**
     * @return the id_disciplina
     */
    public int getId_disciplina() {
        return id_disciplina;
    }

    /**
     * @param id_disciplina the id_disciplina to set
     */
    public void setId_disciplina(int id_disciplina) {
        this.id_disciplina = id_disciplina;
    }

    /**
     * @return the disciplina
     */
    public String getDisciplina() {
        return disciplina;
    }

    /**
     * @param disciplina the disciplina to set
     */
    public void setDisciplina(String disciplina) {
        this.disciplina = disciplina;
    }

    /**
     * @return the id_assunto
     */
    public int getId_assunto() {
        return id_assunto;
    }

    /**
     * @param id_assunto the id_assunto to set
     */
    public void setId_assunto(int id_assunto) {
        this.id_assunto = id_assunto;
    }

    /**
     * @return the assunto
     */
    public String getAssunto() {
        return assunto;
    }

    /**
     * @param assunto the assunto to set
     */
    public void setAssunto(String assunto) {
        this.assunto = assunto;
    }
    
}
